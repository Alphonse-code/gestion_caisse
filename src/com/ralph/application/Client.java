    public void afficher() {
        Container contentPane = getContentPane();
        contentPane.setLayout(null);
        String titre[] = {"ID", "NOM", "VILLE", "ADRESSE", "TELEPHONE"};
        Object enreg[][] = new Object[1][1];
        try {
            rs = db.getNbEnreg("client");
            rs.next();
            int nbLigne = rs.getInt("nbl");
            enreg = new Object[nbLigne][titre.length];
            rs = db.getAll("client");
            int i = 0;
            while (rs.next()) {
                enreg[i][0] = (Object) rs.getString("id_client");
                enreg[i][1] = (Object) rs.getString("nom_client");
                enreg[i][2] = (Object) rs.getString("ville_client");
                enreg[i][3] = (Object) rs.getString("adresse_client");
                enreg[i][4] = (Object) rs.getString("telephone_client");
                i++;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        tableClient.setModel(new DefaultTableModel(enreg, titre));
    }
    
