    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtNomCli.getText().equals("") || txtVilleCli.getText().equals("") || txtAdressCli.getText().equals("") || txtTelCli.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez remplire le champ pour ajouter de client");
        } else {
            String colon[] = {"nom_client", "ville_client", "adresse_client", "telephone_client"};
            String data[] = {txtNomCli.getText(), txtVilleCli.getText(), txtAdressCli.getText(), txtTelCli.getText()};
            System.out.println(db.queryInsert("client", colon, data));
            JOptionPane.showMessageDialog(null, "Les donnés ont été ajoutés avec succès");
        }
        afficher();
        clear();
    }//GEN-LAST:event_btnAddActionPerformed

