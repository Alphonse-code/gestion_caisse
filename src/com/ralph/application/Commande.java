    private void txtCodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodeKeyReleased
        // TODO add your handling code here:

        String colonne[] = {"Nom", "marque", "caracteristique", "Prix"};
        rs = db.sqlSelect(colonne, "produits", "code = '" + txtCode.getText() + "'");

        try {
            while (rs.next()) {
                try {
                    txtNom.setText(rs.getString(1));
                    txtMarque.setText(rs.getString(2));
                    txtCaract.setText(rs.getString(3));
                    txtPrix.setText(rs.getString(4));

                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_txtCodeKeyReleased

