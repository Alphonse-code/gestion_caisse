     public void afficher() {
        Container contentPane = getContentPane();
        contentPane.setLayout(null);
        String titre[] = {"CODE", "NOM", "MARQUE", "CARACTERISTIQUE", "PRIX", "STOCK"};
        Object enreg[][] = new Object[1][1];
        try {
            rs = db.getNbEnreg("produits");
            rs.next();
            int nbLigne = rs.getInt("nbl");
            enreg = new Object[nbLigne][titre.length];
            rs = db.getAll("produits");
            int i = 0;
            while (rs.next()) {
                enreg[i][0] = (Object) rs.getString("code");
                enreg[i][1] = (Object) rs.getString("Nom");
                enreg[i][2] = (Object) rs.getString("marque");
                enreg[i][3] = (Object) rs.getString("Caracteristique");
                enreg[i][4] = (Object) rs.getString("Prix");
                enreg[i][5] = (Object) rs.getString("Stock");
                i++;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        tableAppareille.setModel(new DefaultTableModel(enreg, titre));
    }

