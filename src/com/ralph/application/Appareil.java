    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtNom.getText().equals("") || txtPrix.getText().equals("") || txtStock.getText().equals("") || jComboBox1.getSelectedItem().equals("") || txtAreaCaract.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez remplire tout le champ S.V.P");
        }else{
        try {
            String col[] = {"code", "Nom", "marque", "Caracteristique", "Prix", "Stock"};
            String data[] = {txtID.getText(), txtNom.getText(), jComboBox1.getSelectedItem().toString(), txtAreaCaract.getText(), txtPrix.getText(), txtStock.getText()};
            System.out.println(db.queryInsert("produits", col, data));
            JOptionPane.showMessageDialog(null, "Insertion avec succes");
            afficher();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Echec d'insertion");
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_btnAddActionPerformed

